     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|min:2',
            'genre' => 'required|min:2'
        ]);
        
        $artist = new Artist;
        $artist->name = request('name');
        $artist->genre = request('genre');
        $artist->save();
        return redirect('/artists/');
    }

